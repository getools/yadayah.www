def convert_apostrophes(word):
    """Convert apostrophes to proper Hebrew modifiers."""
    if "'" not in word:
        return word

    # Word starts with apostrophe = Aleph (ʾ)
    if word.startswith("'"):
        # First apostrophe is Aleph
        result = "ʾ" + word[1:]
        # Any remaining mid-word apostrophes are Ayin (ʿ)
        result = result.replace("'", "ʿ")
        return result

    # Mid-word or word-final apostrophes = Ayin (ʿ)
    return word.replace("'", "ʿ")

